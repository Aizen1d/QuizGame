    private void CareerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CareerButtonActionPerformed
        // TODO add your handling code here:
        
        Career career = new Career();
        Operations operation = new Operations();
        Message message = new Message();
        
        //Check if there is existing user
        if (operation.checkIfThereIsUser() == true) {
            this.setVisible(false);
            career.setLocation(this.getLocation());
            career.setVisible(true);
        }
        else {
            JOptionPane.showMessageDialog(null, "Create your username first, click play to create!", "Message", JOptionPane.WARNING_MESSAGE);  
        }
        
    }//GEN-LAST:event_CareerButtonActionPerformed

