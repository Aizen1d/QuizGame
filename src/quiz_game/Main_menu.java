    private void PlayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PlayButtonActionPerformed
        // TODO add your handling code here:
        Message message = new Message();
        Play_Choice playChoice = new Play_Choice();
        Operations operation = new Operations();
        
        PlayButton.setEnabled(false);
        CareerButton.setEnabled(false);
        OptionButton.setEnabled(false);
             
        if (operation.checkIfThereIsUser() == true) {
            
            this.setVisible(false);
            playChoice.setLocation(this.getLocation());
            playChoice.setVisible(true);
        }
        else {
           
            message.setAlwaysOnTop(true);
            message.setVisible(true);
 
        }
        
        message.ExitButton.addActionListener(new ActionListener(){
                public void actionPerformed(ActionEvent a){ 
                     PlayButton.setEnabled(true);
                     CareerButton.setEnabled(true);
                     OptionButton.setEnabled(true);
                    }   
                });
        
        message.ConfirmButton.addActionListener(new ActionListener(){;
                  public void actionPerformed(ActionEvent a){ 
                     proceedToPlayChoice();
                    }   
                });
    }//GEN-LAST:event_PlayButtonActionPerformed
    
